package pl.cubeworld.websocket;

@SuppressWarnings("serial")
public class InstanceObjectException extends Exception {

	public InstanceObjectException() {
		super();
	}

	public InstanceObjectException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InstanceObjectException(String arg0) {
		super(arg0);
	}

	public InstanceObjectException(Throwable arg0) {
		super(arg0);
	}

}
