package pl.cubeworld.websocket;

@SuppressWarnings("serial")
public class WebsocketResourceException extends Exception {

	public WebsocketResourceException() {
		super();
	}

	public WebsocketResourceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public WebsocketResourceException(String arg0) {
		super(arg0);
	}

	public WebsocketResourceException(Throwable arg0) {
		super(arg0);
	}

}
